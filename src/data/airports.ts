export function searchAirports(query: string, limit = 10): Airport[] {
  if (!query || query.length < 1) return AIRPORTS.slice(0, limit);

  const q = query.toLowerCase();
  const results = AIRPORTS.filter(
    (a) =>
      a.iata.toLowerCase().includes(q) ||
      a.name.toLowerCase().includes(q) ||
      a.city.toLowerCase().includes(q) ||
      a.country.toLowerCase().includes(q)
  );

  return results.slice(0, limit);
}

export function getAirportByIata(code: string): Airport | undefined {
  return AIRPORTS.find((a) => a.iata.toUpperCase() === code.toUpperCase());
}

